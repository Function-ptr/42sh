#include <stddef.h>
#include <stdlib.h>

void *my_memmove(void *dest, const void *src, size_t n)
{
    char *sdest = (char*)dest, *ssrc = (char*)src;
    char *tmparr = malloc(sizeof(char) * n);
    for (unsigned long i = 0; i < n; i++)
        *(tmparr + i) = *(ssrc + i);
    for (unsigned long i = 0; i < n; i++)
        *(sdest + i) = *(tmparr + i);
    free(tmparr);
    return ((void*)sdest);
}

