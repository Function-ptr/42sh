#include <stddef.h>

void *my_memcpy(void *restrict dest, const void *restrict src, size_t n)
{
    char *restrict sdest = (char*)dest, *restrict ssrc = (char*)src;
    for (unsigned long i = 0; i < n; i++)
        sdest[i] = ssrc[i];
    return (dest);
}

