#include "line_edition.h"

char *autocomplete_builtin(char *input)
{
    const char *builtins[] = {
            "cd", "setenv", "unsetenv", "env", "exit", "echo", "history", "set",
            "unset", "alias", "unalias", "moai", "which", "where", "prompt_on",
            "prompt_off", "repeat"
    };
    uint32_t num_builtins = sizeof(builtins) / sizeof(builtins[0]);
    uint32_t len = (int)strlen(input);
    for (uint32_t i = 0; i < num_builtins; i++) {
        if (!strncmp(input, builtins[i], len)) {
            return strdup(builtins[i] + len);
        }
    }
    return NULL;
}
