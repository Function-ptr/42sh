#include "line_edition.h"
#include "history.h"

char *autocomplete_from_history(char *input, history_t *history)
{
    int offset = get_offset_from_str(input, history);
    if (offset == -1)
        return NULL;
    char *s = history_get_line_from_offset(history, offset);
    if (s == NULL)
        return NULL;
    char *r = strdup(s + strlen(input));
    free(s);
    return r;
}

