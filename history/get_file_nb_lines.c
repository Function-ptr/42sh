#include "history.h"

size_t get_file_nb_lines(char *filename)
{
    size_t len = strlen(filename);
    char *cmd = calloc(len + 7, sizeof(char));
    strcpy(cmd, "wc -l ");
    strcpy(cmd + 6, filename);
    FILE *fp = popen(cmd, "r");
    free(cmd);
    unsigned long lines = 0;
    if (fp != NULL) {
        fscanf(fp, "%lu", &lines);
        pclose(fp);
    }
    return (size_t)lines;
}
