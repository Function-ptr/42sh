#include "parsing.h"

int array_len(char **arr)
{
    int len = 0;
    for (; arr[len] != NULL; len++);
    return (len);
}

bool is_there_no_command(char **word_array, int *status, char sep, bool pipe_in)
{
    int word_array_len = array_len(word_array);
    if (word_array_len == 0) {
        free(word_array);
        return (true);
    }
    return (false);
}

char *word_array_to_command(char **word_array, int *pos)
{
    int word_array_len = array_len(word_array);
    int nbchars = word_array_len - 1;
    for (int i = 0; i < word_array_len; i++)
        nbchars += strlen(word_array[i]);
    char *newcommand = calloc(nbchars + 2, sizeof(char));
    for (int i = 0; i < word_array_len; i++) {
        int len = strlen(word_array[i]);
        memcpy(&newcommand[*pos], word_array[i], len);
        newcommand[*pos + len] = ' ';
        *pos += len + 1;
        free(word_array[i]);
    }
    return (newcommand);
}

char *remove_spaces_in_command(char *comm, char sep, bool pipe_in, int *status)
{
    for (; *comm == ' ' || *comm == '\t'; comm++);
    char **word_array = my_str_to_word_array(comm, " \t");
    int pos = 0;
    if (is_there_no_command(word_array, status, sep, pipe_in)) {
        if (sep == '|' || pipe_in) {
            *status = 0;
            null_command();
            return NULL;
        }
        return (calloc(1, sizeof(char)));
    }
    char *newcommand = word_array_to_command(word_array, &pos);
    free(word_array);
    newcommand[pos - 1] = 0;
    return (newcommand);
}

