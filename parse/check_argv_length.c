#include "parsing.h"

int is_argv_long_enough(char *command, int cap)
{
    char **argv = my_str_to_word_array(command, " \t");
    int walen = 0;
    for (; argv[walen] != NULL; walen++)
        free(argv[walen]);
    free(argv);
    if (walen < cap) {
        return (0);
    }
    return (1);
}
