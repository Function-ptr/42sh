#include "parsing.h"

void read_input(command_t *command, int *file_descriptors)
{
    while (isatty(0) && printf("? ")) {
        size_t inplen = 1;
        char *input = NULL;
        if (getline(&input, &inplen, stdin) == -1) {
            free(input);
            return;
        }
        int len = strlen(input);
        char *tmp = strndup(input, len - 1);
        if (!strcmp(command->awaited_word, tmp)) {
            free(input);
            free(tmp);
            break;
        }
        dprintf(file_descriptors[1], "%s", input);
        free(input);
        free(tmp);
    }
}

void get_word_wait_input(command_t *cmd)
{
    int file_descriptors[2];
    pipe(file_descriptors);
    cmd->in_fd = file_descriptors[0];
    read_input(cmd, file_descriptors);
    close(file_descriptors[1]);
}

bool parse_and_load_redirections(command_t *command)
{
    if (command == NULL || command->command == NULL)
        return (true);
    int status = get_redirections_file_descriptors(command, command->command);
    int useless_status = 1;
    char *to_free = command->command;
    command->command = remove_spaces_in_command(command->command, \
command->next_separator, command->pipe_in, &useless_status);
    free(to_free);
    if (command->command == NULL || status)
        return (true);
    return (false);
}

int load_redirections_for_command(command_t *command)
{
    if (parse_and_load_redirections(command)) {
        free_commands(&command);
        return (-1);
    }
    if (command->redirect_in_word_wait)
        get_word_wait_input(command);
    return (0);
}
