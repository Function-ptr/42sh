#include <unistd.h>
#include <stdbool.h>

void ambigous_redirection(bool input)
{
    write(2, "Ambiguous", 9);
    if (input)
        write(2, " input ", 7);
    else
        write(2, " output ", 8);
    write(2, "redirect.\n", 10);
}

void missing_name_for_redirection(void)
{
    write(2, "Missing name for redirect.\n", 27);
}

void null_command(void)
{
    write(2, "Invalid null command.\n", 22);
}
