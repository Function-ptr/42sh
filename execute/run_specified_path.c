#include "minishell.h"

int is_path(char *str)
{
    for (int i = 0; str[i] != 0 && str[i] != ' '; i++)
        if (str[i] == '/')
            return 1;
    return 0;
}

int run_specified_path(command_t *cmd, envvar_t **env)
{
    char *command = my_strdup(cmd->command);
    char *binary = get_binary_name(command);
    int status = 0;
    if (!access(binary, F_OK))
        status = run_binary_file(binary, cmd, env);
    else {
        write(2, binary, my_strlen(binary));
        write(2, ": Command not found.\n", 21); return (1);
    }
    free(command);
    return (status);
}
