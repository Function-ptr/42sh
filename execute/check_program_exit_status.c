#include "minishell.h"

void check_if_signaled(int status)
{
    if (WIFSIGNALED(status)) {
        char *sig = strsignal(WTERMSIG(status));
        if (WTERMSIG(status) == SIGFPE)
            write(2, "Floating exception", 18);
        else
            write(2, sig, my_strlen(sig));
        if (WCOREDUMP(status))
            write(2, " (core dumped)", 14);
        write(2, "\n", 1);
    }
}

void check_if_stopped(int status)
{
    if (WIFSTOPPED(status)) {
        char *sig = strsignal(WSTOPSIG(status));
        write(2, sig, my_strlen(sig));
        if (WCOREDUMP(status))
            write(2, " (core dumped)", 14);
        write(2, "\n", 1);
    }
}

int check_exit_status(int status)
{
    check_if_signaled(status);
    check_if_stopped(status);
    return (WIFEXITED(status));
}
