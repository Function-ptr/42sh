#include "minishell.h"

int detect_command_type_and_run(command_t *command, int *exiting,
    envdata_t *env)
{
    int status = 0;
    char *bname = my_strdup(command->command);
    if (!my_strcmp(get_binary_name(bname), "exit"))
        *exiting = 1;
    if (is_a_builtin(get_binary_name(bname))) {
        status = builtin_funcs(command, env);
        free(bname);
        return (status);
    }
    if (!is_path(get_binary_name(bname))) {
        status = execute_from_path(command, env->path_dirs, env->env);
    } else
        status = run_specified_path(command, env->env);
    free(bname);
    return (status);
}
