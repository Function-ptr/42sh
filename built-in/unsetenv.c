#include "built_in.h"

bool does_variable_exist(envvar_t *var, char *variable_name)
{
    bool found = false;
    for (; var != NULL; var = var->next) {
        if (compare_variable_name(var->var, variable_name) == 0) {
            found = true;
            break;
        }
    }
    if (!found) {
        free(variable_name);
        return false;
    }
    return true;
}

void unset_env(envvar_t **env, char *inp)
{
    if (!is_argv_long_enough(inp, 2)) {
        fprintf(stderr, "unsetenv: Too few arguments.\n");
        return;
    }
    char *variable_name = get_variable_name(&inp[9]);
    envvar_t *var = *env, *tmp = *env;
    if (does_variable_exist(var, variable_name) == false)
        return;
    if (var->next == NULL) {
        *env = NULL;
    } else
        tmp->next = var->next;
    free(var->var);
    free(var);
    free(variable_name);
}
