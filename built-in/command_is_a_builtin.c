#include "built_in.h"

bool is_a_builtin(char *binary)
{
    const char *builtins[] = {
        "cd", "setenv", "unsetenv", "env", "exit", "echo", "history", "set", "unset"
    };

    size_t num_builtins = sizeof(builtins) / sizeof(builtins[0]);

    for (size_t i = 0; i < num_builtins; i++) {
        if (strcmp(binary, builtins[i]) == 0) {
            return true;
        }
    }

    return false;
}
