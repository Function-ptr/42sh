#include "environment.h"

aliases_t *init_aliases(void)
{
    aliases_t *aliases = malloc(sizeof(aliases_t));
    aliases->nb_aliases = 0;
    aliases->alias = NULL;
    aliases->content = NULL;
    return aliases;
}

void clean_aliases(aliases_t *aliases)
{
    for (uint32_t i = 0; i < aliases->nb_aliases; i++) {
        free(aliases->alias[i]);
        free(aliases->content[i]);
    }
    free(aliases->alias);
    free(aliases->content);
    free(aliases);
}
