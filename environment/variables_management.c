#include "environment.h"

variables_t *init_variables(void)
{
    variables_t *variables = malloc(sizeof(variables_t));
    variables->nb_variables = 0;
    variables->names = NULL;
    variables->values = NULL;
    return variables;
}

void clean_variables(variables_t *variables)
{
    for (size_t i = 0; i < variables->nb_variables; i++) {
        free(variables->names[i]);
        free(variables->values[i]);
    }
    free(variables->values);
    free(variables->names);
    free(variables);
}
