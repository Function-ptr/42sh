#include "minishell.h"

void add_environment_variable(envvar_t **env, char *var)
{
    envvar_t *envvar = malloc(sizeof(envvar_t));
    if (envvar == NULL) {
        clear_environment_variables(env);
        exit(84);
    }
    envvar->var = strdup(var);
    if (envvar->var == NULL) {
        free(envvar);
        clear_environment_variables(env);
        exit(84);
    }
    envvar->next = *env;
    *env = envvar;
}

void clear_environment_variables(envvar_t **list)
{
    envvar_t *var = *list;
    while (var != NULL) {
        envvar_t *tmp = var->next;
        free(var->var);
        free(var);
        var = tmp;
    }
    free(list);
}

void reverse_environment_variables(envvar_t **begin)
{
    envvar_t *tmp = *begin;
    envvar_t *prev = NULL;
    envvar_t *next;

    while (tmp != NULL) {
        next = tmp->next;
        tmp->next = prev;
        prev = tmp;
        tmp = next;
    }
    *begin = prev;
}
